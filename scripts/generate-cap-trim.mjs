#!/usr/bin/env node
/**
 * Generates the SCSS `cap-trim` mixin from the shipped Lato metrics using
 * @capsizecss/core, so vertical centering can be derived from cap-height rather
 * than from the font's declared ascent/descent line box. See
 * shell/assets/styles/base/_mixins.scss consumers and the typography follow-up
 * issue for the rationale.
 *
 * Run: `yarn cap-trim:generate`
 *
 * The emitted values are the leading-trim margins Capsize computes; they are
 * em-relative and therefore font-size independent (one mixin fits every size).
 *
 * IMPORTANT: the fallback path is only reached in engines without
 * `text-box-trim` (Firefox through at least v149). Those engines position the
 * line box from the metrics the browser actually uses, so the metrics below
 * MUST match the *shipped* font:
 *   - current vendored Lato has USE_TYPO_METRICS cleared -> browser uses hhea
 *     (ascent 1974, descent -426, lineGap 0).
 *   - if the fonts are ever restored to the unmodified upstream build
 *     (USE_TYPO_METRICS set), switch to the sTypo* metrics
 *     (ascent 1610, descent -390, lineGap 400) and re-run this script.
 * The CI font-metric guard (typography follow-up issue) asserts these stay true.
 */
import { precomputeValues } from '@capsizecss/core';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Lato metrics (font units, unitsPerEm 2000). capHeight/xHeight from OS/2.
// Matches the currently shipped (USE_TYPO_METRICS-cleared) Lato -> hhea box.
const latoMetrics = {
  familyName: 'Lato',
  category:   'sans-serif',
  unitsPerEm: 2000,
  ascent:     1974,
  descent:    -426,
  lineGap:    0,
  capHeight:  1433,
  xHeight:    1013,
};

// Font size is arbitrary here: Capsize's trims are em-relative, so any size
// yields the same em values that then scale to every button/text size.
const { capHeightTrim, baselineTrim } = precomputeValues({ fontSize: 16, fontMetrics: latoMetrics });

const out = `// -----------------------------------------------------------------------------
// GENERATED by scripts/generate-cap-trim.mjs — do not edit by hand.
// Run \`yarn cap-trim:generate\` to regenerate from the shipped Lato metrics.
// -----------------------------------------------------------------------------

/// Trims a text element's leading down to its cap-height/alphabetic-baseline box
/// so that flex/grid centering aligns the *letters*, independent of the font's
/// declared ascent/descent. Uses native \`text-box-trim\` where available and a
/// Capsize-computed leading-trim fallback (Firefox) everywhere else.
///
/// Apply to the text element itself (e.g. a label span), not to a flex row that
/// also contains icons — the fallback's ::before/::after would become flex items.
@mixin cap-trim {
  // Chrome 133+, Safari 18.2+: trim to the cap box in-engine (metric-agnostic).
  text-box-trim: trim-both;
  text-box-edge: cap alphabetic;

  // Fallback (Firefox through >= v149): Capsize leading-trim for Lato.
  @supports not (text-box-trim: trim-both) {
    display: block;
    line-height: normal;

    &::before,
    &::after {
      content: '';
      display: table;
    }

    &::before {
      margin-bottom: ${ capHeightTrim }; // cap-height trim (ascent - capHeight)
    }

    &::after {
      margin-top: ${ baselineTrim }; // baseline trim (descent)
    }
  }
}
`;

const target = resolve(__dirname, '../shell/assets/styles/base/_cap-trim.generated.scss');

writeFileSync(target, out);
// eslint-disable-next-line no-console
console.log(`cap-trim: wrote ${ target }\n  capHeightTrim ${ capHeightTrim }  baselineTrim ${ baselineTrim }`);
