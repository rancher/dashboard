#!/usr/bin/env node
/**
 * Generates the SCSS `cap-trim` mixin from the *shipped* Lato metrics so vertical
 * centering can be derived from cap-height rather than from the font's declared
 * ascent/descent line box. See shell/assets/styles/base/_mixins.scss consumers
 * and the typography follow-up issue for the rationale.
 *
 * Run: `yarn cap-trim:generate`
 *
 * The emitted values are the leading-trim margins Capsize computes; they are
 * em-relative and therefore font-size independent (one mixin fits every size).
 *
 * The fallback path is only reached in engines without `text-box-trim` (Firefox
 * through at least v149). Those engines position the line box from the metrics
 * the browser actually uses, which depends on the OS/2 USE_TYPO_METRICS flag:
 *   - flag SET   -> browser uses the sTypo* metrics.
 *   - flag CLEAR -> browser uses the hhea metrics.
 * So we read the shipped font directly (fontkitten) and pick the matching box.
 * This means the workflow "swap the font, re-run this script" is self-correcting
 * whether the vendored Lato is the unmodified upstream build (flag set) or a
 * USE_TYPO_METRICS-cleared patch (flag clear) — no hand-maintained metrics.
 */
import { precomputeValues } from '@capsizecss/core';
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { createRequire } from 'node:module';

// fontkitten is CommonJS with a named `create` export.
const require = createRequire(import.meta.url);
const { create } = require('fontkitten');

const __dirname = dirname(fileURLToPath(import.meta.url));

// The font families all share Lato's metrics, so Regular is representative.
const fontPath = resolve(__dirname, '../shell/assets/fonts/lato/LatoLatin-Regular.woff2');
const font = create(readFileSync(fontPath));
const os2 = font['OS/2'];
const useTypoMetrics = os2.fsSelection.useTypoMetrics;

// Pick the box the browser will actually lay text out with.
const fontMetrics = {
  familyName: 'Lato',
  category:   'sans-serif',
  unitsPerEm: font.head.unitsPerEm,
  capHeight:  os2.capHeight,
  xHeight:    os2.xHeight,
  ascent:     useTypoMetrics ? os2.typoAscender : font.hhea.ascent,
  descent:    useTypoMetrics ? os2.typoDescender : font.hhea.descent,
  lineGap:    useTypoMetrics ? os2.typoLineGap : font.hhea.lineGap,
};

// Font size is arbitrary here: Capsize's trims are em-relative, so any size
// yields the same em values that then scale to every button/text size.
const { capHeightTrim, baselineTrim } = precomputeValues({ fontSize: 16, fontMetrics });

const box = useTypoMetrics ? 'sTypo (USE_TYPO_METRICS set)' : 'hhea (USE_TYPO_METRICS cleared)';

const out = `// -----------------------------------------------------------------------------
// GENERATED by scripts/generate-cap-trim.mjs — do not edit by hand.
// Run \`yarn cap-trim:generate\` to regenerate from the shipped Lato metrics.
// Source: LatoLatin-Regular.woff2, ${ box } box.
// -----------------------------------------------------------------------------

/// Trims a text element's leading down to its cap-height/alphabetic-baseline box
/// so that flex/grid centering aligns the *letters*, independent of the font's
/// declared ascent/descent. Uses native \`text-box-trim\` where available and a
/// Capsize-computed leading-trim fallback (Firefox) everywhere else.
///
/// Apply to the text element itself (e.g. a label span), not to a flex row that
/// also contains icons — the fallback's ::before/::after would become flex items.
@mixin cap-trim {
  // Chrome 133+, Safari 18.2+: trim to the cap box in-engine (metric-agnostic).
  text-box-trim: trim-both;
  text-box-edge: cap alphabetic;

  // Fallback (Firefox through >= v149): Capsize leading-trim for Lato.
  @supports not (text-box-trim: trim-both) {
    display: block;
    line-height: normal;

    &::before,
    &::after {
      content: '';
      display: table;
    }

    &::before {
      margin-bottom: ${ capHeightTrim }; // cap-height trim (ascent - capHeight)
    }

    &::after {
      margin-top: ${ baselineTrim }; // baseline trim (descent)
    }
  }
}
`;

const target = resolve(__dirname, '../shell/assets/styles/base/_cap-trim.generated.scss');

writeFileSync(target, out);
// eslint-disable-next-line no-console
console.log(`cap-trim: ${ box }\n  wrote ${ target }\n  capHeightTrim ${ capHeightTrim }  baselineTrim ${ baselineTrim }`);
