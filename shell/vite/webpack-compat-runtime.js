/**
 * Browser-side helpers backing the webpack compatibility transforms in
 * shell/vite/webpack-compat-plugin.ts. They emulate webpack's
 * require.context / dynamic require semantics on top of import.meta.glob maps.
 */

function moduleNotFound(request) {
  const error = new Error(`Cannot find module '${ request }'`);

  error.code = 'MODULE_NOT_FOUND';

  return error;
}

/**
 * Emulates webpack's require.context(dir, recursive, regExp).
 *
 * @param modules eagerly globbed modules, keyed by root-relative path
 * @param prefix  root-relative directory prefix (with trailing slash) to strip so keys become './<rel>'
 * @param regExp  the filter regular expression from the original require.context call
 */
export function createRequireContext(modules, prefix, regExp) {
  const map = {};

  Object.keys(modules).forEach((key) => {
    const relative = `./${ key.substr(prefix.length) }`;

    if (regExp.test(relative)) {
      map[relative] = modules[key];
    }
  });

  const context = (request) => {
    if (request in map) {
      return map[request];
    }

    throw moduleNotFound(request);
  };

  context.keys = () => Object.keys(map);
  context.resolve = (request) => {
    if (request in map) {
      return request;
    }

    throw moduleNotFound(request);
  };
  context.id = prefix;

  return context;
}

function resolveKey(modules, prefix, name, suffix) {
  const base = prefix + name + suffix;
  const candidates = suffix ? [base] : [
    base,
    `${ base }.js`,
    `${ base }.ts`,
    `${ base }.vue`,
    `${ base }.yaml`,
    `${ base }/index.vue`,
    `${ base }/index.js`,
    `${ base }/index.ts`,
  ];

  return candidates.find((candidate) => candidate in modules);
}

/**
 * Emulates a synchronous webpack require with a dynamic path, e.g. require(`@shell/models/${ type }`).
 * The backing glob map must be eager.
 */
export function dynamicRequire(modules, prefix, name, suffix = '') {
  const key = resolveKey(modules, prefix, name, suffix);

  if (!key) {
    throw moduleNotFound(prefix + name + suffix);
  }

  return modules[key];
}

/**
 * Emulates a dynamic import with a variable path, e.g. import(`@shell/pages/${ name }.vue`).
 * The backing glob map is lazy, so this returns a promise for the module.
 */
export function dynamicImport(modules, prefix, name, suffix = '') {
  const key = resolveKey(modules, prefix, name, suffix);

  if (!key) {
    return Promise.reject(moduleNotFound(prefix + name + suffix));
  }

  return modules[key]();
}

/**
 * Vite flavour of the auto-import registration generated by shell/pkg/auto-import.js
 * (see the import.js template used with webpack). Registers every file of a package
 * context folder (models, edit, list etc) with the extension manager.
 *
 * Models are passed as an eager glob map (lookups are synchronous), everything else
 * as a lazy glob map (matching webpack's `() => import(...)` registrations).
 */
export function registerContextFolder($extension, type, modules, prefix) {
  Object.keys(modules).forEach((key) => {
    let name = key.substr(prefix.length);
    const ext = name.lastIndexOf('.');

    name = name.substr(0, ext);

    const mod = modules[key];
    const fn = typeof mod === 'function' ? mod : () => mod;

    $extension.register(type, name, fn);
  });
}
